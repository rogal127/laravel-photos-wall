Przed Wami aplikacja z grą terenową o gminie Świdnica!! Wystarczy, że zarejestrujecie się w naszej apce i bez ograniczeń możecie z niej korzystać zarówno na smartfonie, jak i na komputerze! Pamiętajcie, by przed założeniem konta zapoznać się z regulaminem oraz polityką prywatności naszej strony. <br /><br />
Jako użytkownik naszej aplikacji możesz wziąć udział w świetnej zabawie, przygotowanej przez młodzież w ramach projektu „Apka, Świdnicka Mapka”. Na czym ona polega? To proste! <br /><br />
Przejrzyj zdjęcia, które dla Was wykonaliśmy. Przedstawiają one szczegóły niezwykłych miejsc w gminie Świdnica. Waszym zadaniem jest odgadnięcie, co to za lokalizacja i wykonanie na miejscu podobnego zdjęcia swoim smartfonem. W rozwiązaniu zagadki mogą być pomocne opisy pod poszczególnymi zdjęciami.<br /><br />
Gdy zbierzecie wszystkie fotografie, udajcie się niezwłocznie do Gminnego Ośrodka Kultury w Świdnicy, gdzie czekać będzie na Was nagroda. Warunkiem otrzymania upominku jest poprawna weryfikacja Waszych zdjęć! <br /><br /><br />
