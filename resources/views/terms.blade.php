2. W celu prawidłowego korzystania z Fotoportalu wymagane jest:<br />
<div style="margin-left: 3%">
·     połączenie z siecią Internet,<br />

·     przeglądarka internetowa:<br />

·     Chrome, Firefox lub Opera w jednej z trzech ostatnich wersji,<br />

·     Safari lub Edge w jednej z dwóch ostatnich wersji,<br />

·     Internet Explorer w wersji 11,<br />

·     obsługiwanie plików cookie w przeglądarce.<br />
</div>
<center><h3>§3 REJESTRACJA</h3></center><br />


1. Rejestracja w Fotoportalu jest bezpłatna i dobrowolna, ale niezbędna do korzystania z Fotoportalu.<br />

2. Rejestracja w Fotoportalu jest jednoznaczne z akceptacją Regulaminu.<br />

3. Podczas rejestracji użytkownik zobowiązany jest podać swoje dane osobowe. Informacje te są niezbędne dla ustalenia praw autorskich do zdjęć zgłaszanych do Fotoportalu.<br />

4. Administrator informuje, że podszywanie się pod inną osobę podlega odpowiedzialności karnej.<br />

 
<center><h3>§4 ZGŁOSZENIE UTWORÓW</h3></center><br />


1. Zgłoszenie utworu do Fotoportalu następuje w momencie zamieszczenia zdjęcia na stronie: www.fotoportal.gokswidnica.pl.<br />

2. Na stronie, o której mowa w ust. 1, należy zamieszczać pliki w formacie JPG                     o możliwie maksymalnej wielkości i rozdzielczości: 5MB.<br />

3. Tytuł utworu powinien zawierać informację o miejscu, w którym zostało wykonane.<br />

4. Utwory powinny przedstawiać gminę Świdnica. <br />

5. Każdy utwór musi zostać zaakceptowany przez Administratora.<br />

6. Administrator zastrzega sobie prawo do nieakceptacji utworów, które nie będą spełniały wymagań niniejszego regulaminu.<br />

7. Użytkownik może zgłaszać tylko utwory, do których posiada prawa autorskie. <br />

8. Administrator informuje, że podszywanie się pod autora cudzych zdjęć podlega odpowiedzialności karnej.<br />

<center><h3>§ 5 WIZERUNEK OSÓB TRZECICH</h3></center><br />


1. Udzielenie zgody na wykorzystanie wizerunku osób trzecich na zdjęciu pozostaje po stronie Użytkownika. Administrator nie ponosi odpowiedzialności za udostępnienie utworu bez zgody modela/modelki.<br />

2. Administrator zastrzega sobie prawo do nieakceptacji zdjęć, którego tematem są alkohol, przemoc, nagość, itp.<br />

3. Użytkownik zapewnia, że w pozostałym zakresie zgłoszone przez niego zdjęcia nie będą naruszały dóbr osobistych innych osób.<br />

4. W zakresie powyższych oświadczeń Użytkownik ponosi względem Gminnego Ośrodka Kultury pełną odpowiedzialność odszkodowawczą obejmująca w szczególności szkodę bezpośrednią, koszty sądowe, koszty pomocy prawnej świadczonej w celu obrony interesów Gminnego Ośrodka Kultury oraz równowartość świadczeń spełnionych przez Gminny Ośrodek Kultury w celu zaspokojenia roszczeń osób trzecich, których wizerunek znajduje się na zgłoszonych do konkursu zdjęciach z tytułu naruszenia ich dóbr osobistych. Powyższe nie zwalnia Użytkownika z obowiązku współdziałania z Gminnym Ośrodku Kultury w celu skutecznej obrony przed roszczeniami takich osób trzecich dotyczących naruszenia praw do ich wizerunku.<br /><br />


<center><h3>§6 PRAWA AUTORSKIE</h3></center><br />


1. W momencie zgłoszenia przez Użytkownika utworu, na Administratora nieodpłatnie przechodzą autorskie prawa majątkowe oraz prawa pokrewne w zakresie następujących pól eksploatacji:<br />
<div style="margin-left: 3%">
a) udostępniania ich innym osobom w celach komercyjnych i niekomercyjnych                     w publikacjach i opracowaniach opisujących gminę Świdnica;<br />

b) prawo do trwałego lub czasowego zwielokrotnienia utworu w całości lub w części, jakimikolwiek środkami i w jakiejkolwiek formie;<br />

c) prawo do wykorzystania utworu i jego modyfikacji oraz adaptacji we wszelakiego rodzaju dostępnych formach, m.in. w środkach reklamy, w tym reklamy telewizyjnej, radiowej, prasowej, internetowej, reklamy zewnętrznej (Outdoor), materiałach reklamowych nie przeznaczonych do prezentacji w mediach (BTL), plakatach, ulotkach <br />
</div>
2. Użytkownik Fotoportalu oświadcza, że utwór nie jest obciążony prawami osób trzecich, a w  szczególności, że własnościowe oraz osobiste i majątkowe prawa autorskie do utworów przysługują Użytkownikowi zakresie wskazanym w ust. 1. <br />

 

<center><h3>§7 UDOSTĘPNIANIE ZDJĘĆ</h3></center><br />


1. Każdy użytkownik ma prawo zagłosować pozytywnie lub negatywnie na zdjęcia,          które znajdują się w bazie Fotoportalu, a także udostępnić je w przestrzeni internetowej.<br />

2. Pod wtórnie udostępnionym zdjęciem każdorazowo należy podpisać autora zdjęcia.<br />

 
<center><h3>§8 DANE OSOBOWE</h3></center><br />


1. Administratorem danych osobowych Użytkowników jest Dyrektor Gminnego Ośrodka Kultury, ul. Ogrodowa 37, 66-008 Świdnica e-mail: dyrektor@gokswidnica.pl,                      tel.: +48 683273322.<br />

2. Dane osobowe Użytkowników przetwarzane będą przez Administratora w celu komunikacji z Użytkownikami i umożliwienia im korzystania z Fotoportalu. Przetwarzanie danych osobowych odbywać się będzie na zasadach przewidzianych ustawą z dnia 10 maja 2018 roku o ochronie danych osobowych (t.j. Dz. U. 2019 poz. 1781) Dane osobowe  podawane są dobrowolnie, jednak jest to niezbędne w celu prawidłowej realizacji postanowień Regulaminu, a Uczestnicy mają prawo do wglądu do swoich danych oraz do ich poprawiania i usunięcia<br />

 
<center><h3>§9 DEZAKTYWACJA KONTA</h3></center><br />


1.Użytkownik ma prawo w każdej chwili dezaktywować konto na Fotoportalu.<br />

2.Dezaktywacja konta nie jest jednoznaczna z usunięciem z Fotoportalu utworu, zamieszczonych przez Użytkownika.<br />

3.Aby dezaktywować konto należy każdorazowo wysłać wiadomość e-mail na adres sekretariat@gokswidnica.pl.<br />

 
<center><h3>§10 POSTANOWIENIA KOŃCOWE</h3></center><br />


