2.Administrator zastrzega sobie prawo do zmiany regulaminu.<br />

3.W sprawach nieuregulowanych niniejszym regulaminem stosuje się właściwe przepisy.<br />

4.W sprawach spornych, dotyczących interpretacji Regulaminu, decyzję podejmuje Administrator.<br />

6.Regulamin obowiązuje od dnia 27 marca 2021 roku.<br />


